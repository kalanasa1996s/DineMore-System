    private void loadAllOrders() {
        dtm = (DefaultTableModel) tblchef.getModel();
        dtm.setRowCount(0);

        try {
            List<OrderDTO> orders = ManageOrderController.getAllCustomers();
            if (orders == null) {
                return;
            }
            for (OrderDTO order : orders) {
                Object rowdata[] = {
                    order.getOrder_id(),
                    order.getReception_id(),
                    order.getDate(),
                    order.getTime(),
                    order.getCus_name(),
                    order.getTp_no(),
                    order.getQty()
                };
                dtm.addRow(rowdata);
            }
        } catch (Exception ex) {
            Logger.getLogger(Chef_view.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void getAllOrders() {
        
        
            dtm = (DefaultTableModel) tblchef.getModel();
        dtm.setRowCount(0);
        try {
            List<OrderDTO> orders = ManageOrderController.getAllCustomers();
            if (orders == null) {
                return;
            }

            for (OrderDTO orderDTO : orders) {
                Object[] rowData = {
                    orderDTO.getOrder_id(),
                    orderDTO.getReception_id(),
                    orderDTO.getDate(),
                    orderDTO.getTime(),
                    orderDTO.getCus_name(),
                    orderDTO.getTp_no(),
                    orderDTO.getQty(),};
                dtm.addRow(rowData);
            }

        } catch (Exception ex) {
            Logger.getLogger(View.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }

    
