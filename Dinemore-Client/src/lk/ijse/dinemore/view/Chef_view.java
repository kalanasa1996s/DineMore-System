    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            String ststus = "Cooking";
            OrderDTO orderDTO = new OrderDTO(
                    txtoid.getText(),
                    txtrid.getText(),
                    txtdate.getText(),
                    txttime.getText(),
                    txtcustname.getText(),
                    txttp.getText(),
                    Integer.parseInt(txtqty.getText()),
                    ststus
            );
            boolean addorder = ManageOrderController.updateCustomer(orderDTO);
            if (addorder) {
                JOptionPane.showMessageDialog(this, "Order has been taken Successfully");
                loadAllOrders();

                getAllOrders();
//                clearText();
            } else {
                JOptionPane.showMessageDialog(this, "Order faild");
            }
        } catch (Exception ex) {
            Logger.getLogger(Chef_view.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

